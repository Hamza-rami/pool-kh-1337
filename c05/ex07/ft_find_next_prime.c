int	ft_find_next_prime(int nb)
{
	int	i;
	int	j;

	j = 2;
	i = 2;
	while (i < nb)
	{
		if (nb % i == 0)
		{
			while (j < nb)
			{
				if (nb % j == 0)
				{
					nb++;
					j = 2;
				}
				j++;
			}
			return (nb);
		}
		i++;
	}
	return (i);
}
